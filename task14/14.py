# только +++

a = 25 ** 3 + 5 ** 9 + 125
k0 = 0
while a > 0:
    if a % 5 == 0:
        k0 += 1
    a = a // 5
print(k0)

# +++ и двойные ---
# сколько цифр 6 -> ищем остаток 6
a = 4 ** 18 * 4 ** 10 - 4 ** 6 - 16
k0 = 0
while a > 0:
    if a % 4 == 3:
        k0 += 1
    a = a // 4
print(k0)

# двоичная система
a = 4 ** 1014 + 2 ** 1012 - 7
k0 = 0
while a > 0:
    if a % 2 == 1:
        k0 += 1
    a = a // 2
print(k0)
